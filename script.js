// Function to show the correct page when clicked
function showPage(pageId) {
    const pages = document.querySelectorAll('.container > div');
    pages.forEach(page => {
        if (page.id === pageId) {
            page.style.display = 'block';
        } else {
            page.style.display = 'none';
        }
    });

    if (pageId === 'duasPage') {
        renderDuas();
    } else if (pageId === 'surahsPage') {
        renderSurahs();
    } else if (pageId === 'favoritesPage') {
        renderFavorites();
    } else if (pageId === 'dailyDuasPage') {
        renderDailyDuas();
    } else if (pageId === 'dailySurahsPage') {
        renderDailySurahs();
}
}

// Function to render the list of duas
function renderDuas() {
    const duasList = document.getElementById('duasList');
    duasList.innerHTML = '';
    duas.forEach(dua => {
        const duaCard = createDuaCard(dua, 'dua');
        duasList.appendChild(duaCard);
    });
}
function renderSurahs() {
    const surahsList = document.getElementById('surahsList');
    surahsList.innerHTML = '';

    surahs.forEach(surah => {
        const surahButton = document.createElement('button');
        surahButton.className = 'nav-btn';
        surahButton.innerText = surah.name;
        
        // Add an event listener to open the file in a new tab
        surahButton.addEventListener('click', function() {
            window.open(surah.file, '_blank');
        });

        surahsList.appendChild(surahButton);
    });
}

// Function to render favorite duas
function renderFavorites() {
    const favoritesList = document.getElementById('favoritesList');
    favoritesList.innerHTML = '';
    const favoriteDuas = [...duas, ...dailyDuas].filter(dua => dua.favorite); // Combine duas and dailyDuas and filter favorites
    if (favoriteDuas.length === 0) {
        favoritesList.innerHTML = '<p>You have no favorite duas yet.</p>';
    } else {
        favoriteDuas.forEach(dua => {
            const duaCard = createDuaCard(dua, dua.hasOwnProperty('reason') ? 'dailyDua' : 'dua');
            favoritesList.appendChild(duaCard);
        });
    }
}

// Function to create a single dua card
function createDuaCard(dua, type) {
    const duaCard = document.createElement('div');
    duaCard.className = 'content-card';

    if (type === 'dua') {
        duaCard.innerHTML = `
            <p class="arabic">${dua.arabic}</p>
            <p class="translation">${dua.translation}</p>
            <p class="reference">${dua.reference}</p>
            <div class="card-actions">
                <button class="card-btn favorite-btn" onclick="toggleFavorite(${dua.id}, 'duas')">
                    <i class="fas fa-heart ${dua.favorite ? 'favorite' : ''}"></i>
                </button>
                <button class="card-btn edit-btn" onclick="openEditDuaModal(${dua.id}, 'duas')">
                    <i class="fas fa-edit"></i>
                </button>
            </div>
        `;
    } else if (type === 'dailyDua') {
        duaCard.innerHTML = `
            <p class="arabic">${dua.name}</p>
            <p class="translation">${dua.reason}</p>
            <p class="reference">${dua.reference}</p>
            <div class="card-actions">
                <button class="card-btn favorite-btn" onclick="toggleFavorite(${dua.id}, 'dailyDuas')">
                    <i class="fas fa-heart ${dua.favorite ? 'favorite' : ''}"></i>
                </button>
            </div>
        `;
    }

    return duaCard;
}

// Function to toggle the favorite status of a dua
function toggleFavorite(duaId, listName) {
    const list = listName === 'duas' ? duas : dailyDuas;
    const dua = list.find(d => d.id === duaId);
    if (dua) {
        dua.favorite = !dua.favorite; // Toggle the favorite status
        if (listName === 'duas') {
            renderDuas();
            renderFavorites();
        } else {
            renderDailyDuas();
            renderFavorites();
        }
    }
}

// Function to open the modal for adding a new dua
function openAddDuaModal() {
    document.getElementById('modalTitle').textContent = 'Add New Dua';
    document.getElementById('duaForm').reset();
    document.getElementById('duaId').value = '';
    document.getElementById('addEditModal').style.display = 'block';
}

// Function to open the modal for editing an existing dua
function openEditDuaModal(duaId, listName) {
    const list = listName === 'duas' ? duas : dailyDuas;
    const dua = list.find(d => d.id === duaId);
    if (dua) {
        document.getElementById('modalTitle').textContent = 'Edit Dua';
        document.getElementById('duaId').value = dua.id;
        document.getElementById('arabicText').value = dua.arabic;
        document.getElementById('translation').value = dua.translation;
        document.getElementById('reference').value = dua.reference;
        document.getElementById('addEditModal').style.display = 'block';
    }
}

// Close the modal when clicking the close button
document.querySelector('.close').onclick = function() {
    document.getElementById('addEditModal').style.display = 'none';
}

// Close the modal when clicking outside the modal
window.onclick = function(event) {
    if (event.target == document.getElementById('addEditModal')) {
        document.getElementById('addEditModal').style.display = 'none';
    }
}

// Handle the form submission for adding/editing a dua
document.getElementById('duaForm').onsubmit = function(e) {
    e.preventDefault();
    const duaId = document.getElementById('duaId').value;
    const newDua = {
        id: duaId ? parseInt(duaId) : Date.now(),
        arabic: document.getElementById('arabicText').value,
        transliteration: document.getElementById('transliteration').value,
        translation: document.getElementById('translation').value,
        reference: document.getElementById('reference').value,
        favorite: false
    };

    if (duaId) {
        const index = duas.findIndex(d => d.id === parseInt(duaId));
        if (index !== -1) {
            duas[index] = { ...duas[index], ...newDua };
        }
    } else {
        duas.push(newDua);
    }

    document.getElementById('addEditModal').style.display = 'none';
    renderDuas();
    renderFavorites();
}

// Handle theme toggle functionality
document.addEventListener('DOMContentLoaded', function() {
    const themeToggle = document.getElementById('themeToggle');

    themeToggle.addEventListener('click', function() {
        document.body.classList.toggle('light-theme');
        const icon = this.querySelector('i');
        icon.classList.toggle('fa-moon');
        icon.classList.toggle('fa-sun');
    });

    showPage('homePage');
});

