function renderDailySurahs() {
    const dailySurahsList = document.getElementById('dailySurahsList');
    dailySurahsList.innerHTML = '';
    dailySurahs.forEach(surah => {
        const surahCard = document.createElement('div');
        surahCard.className = 'content-card';
        surahCard.innerHTML = `
            <h3>${surah.name}</h3>
            <p class="arabic">${surah.arabic}</p>
            <p class="reference">${surah.reference}</p>
        `;
        dailySurahsList.appendChild(surahCard);
    });
}
